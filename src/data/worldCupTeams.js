const BY_CODE = new Map(WORLD_CUP_TEAMS.map((team) => [team.code, team]));
const BY_SEED_NAME = new Map(WORLD_CUP_TEAMS.map((team) => [team.seedName, team]));
const BY_DISPLAY_NAME = new Map(WORLD_CUP_TEAMS.map((team) => [team.displayName, team]));

export function getTeamPresentationByCode(code) {
  return BY_CODE.get(code) || null;
}

export function getTeamPresentationByName(name) {
  return BY_SEED_NAME.get(name) || BY_DISPLAY_NAME.get(name) || null;
}

export function teamAnchorId(nameOrCode) {
  const team = getTeamPresentationByCode(nameOrCode) || getTeamPresentationByName(nameOrCode);
  return `equipo-${slugifyTeam(team?.displayName || nameOrCode)}`;
}

export function isPlaceholderTeamName(name) {
  return !name || name === 'TBD' || /^[12][A-L]$/.test(name) || /^#[A-L](?:\/[A-L])+$/.test(name) || /^[WL]\d{1,3}$/.test(name);
}

export function decorateTeam({ name, code, flag }) {
  const team = getTeamPresentationByCode(code) || getTeamPresentationByName(name);
  if (team) {
    return {
      name: team.displayName,
      code: team.code,
      flag: team.flag,
      anchorId: teamAnchorId(team.code),
      isPlaceholder: false,
    };
  }
  return {
    name,
    code,
    flag,
    anchorId: teamAnchorId(name),
    isPlaceholder: isPlaceholderTeamName(name),
  };
}

export function slugifyTeam(value) {
  return String(value)
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/-{2,}/g, '-')
    .replace(/^-|-$/g, '');
}
